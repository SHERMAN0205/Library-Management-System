    private void searchstudent1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_searchstudent1MouseClicked
    try {
        // TODO add your handling code here:
        String sql2="select * from issue where bookid=?";
        PreparedStatement pst2=con.prepareStatement(sql2);
        pst2.setString(1, Bid3.getText());
        rs=pst2.executeQuery();
        if(rs.next()){
             Bid3.setText(rs.getString("bookid"));
             title3.setText(rs.getString("booktitle"));
             id1.setText(rs.getString("studentid"));
             name1.setText(rs.getString("studentname"));
             phone1.setText(rs.getString("phone"));
        } else{
            JOptionPane.showMessageDialog(null, "this book has not been issued to any student");
        }
    } catch (SQLException ex) {
        Logger.getLogger(main.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_searchstudent1MouseClicked

